<script setup>
const supabase = useSupabaseClient();
const route = useRoute();
const buku = ref([]);

const getBookById = async () => {
  const { data, error } = await supabase.from("Buku").select(`*, Kategori-Buku(*)`).eq("id", route.params.id);
  if (data) buku.value = data[0];
};

onMounted(() => {
  getBookById();
});
</script>
